# quit app  
gsettings set org.cinnamon.desktop.keybindings.wm close "['<Super>q']"

# email
gsettings set org.cinnamon.desktop.keybindings.media-keys email "['<Super>e']"

# home folder
gsettings set org.cinnamon.desktop.keybindings.media-keys home "['<Super>f']"

# terminal
gsettings set org.cinnamon.desktop.keybindings.media-keys terminal "['<Super>t']"

# browser
gsettings set org.cinnamon.desktop.keybindings.media-keys www "['<Super>b']"